#ifndef  _QUEUE_H
#define  _QUEUE_H
/******************************************************************
*                             头文件                              *
******************************************************************/
#include <stdint.h>

/******************************************************************
*                           宏定义                                *
******************************************************************/
#define QUEUE_DATABUF_MAX		0x100

/******************************************************************
*                           结构体定义                          *
******************************************************************/
typedef void * QUEUE_HANDLE;

typedef struct _QUEUE_DATA
{
    uint32_t 	DataLength;
    uint8_t  	DataBuf[QUEUE_DATABUF_MAX];
} QUEUE_DATA, *PQUEUE_DATA;

typedef struct  _QUEUE_NODE
{
    struct _QUEUE_NODE *Next;
    QUEUE_DATA Data;
} QUEUE_NODE;

typedef struct
{
    QUEUE_NODE *Head;
    QUEUE_NODE *Tail;
    //U32 Count;
} QUEUE, *PQUEUE;


/******************************************************************
*                           全局变量声明                          *
******************************************************************/




/******************************************************************
*                         全局函数声明                            *
******************************************************************/


/*
 ************************************************************************************************************************************************************************
 *函数名称: QueueListInit
 *功能描述: 完成队列初始化
 *输入描述: 队列描述符
 *输出描述: 无
 *返回描述: TRUE成功/FALSE失败
 *作者日期: XFW
 *全局声明: 无
 *特殊说明: malloc堆空间，未释放
 ************************************************************************************************************************************************************************
 */
extern uint32_t QueueListInit(QUEUE **Queue);


/*
 ************************************************************************************************************************************************************************
 *函数名称: QueueListDeInit
 *功能描述: 完成队列反初始化
 *输入描述: 队列描述符
 *输出描述: 无
 *返回描述: TRUE成功/FALSE失败
 *作者日期: XFW
 *全局声明: 无
 *特殊说明: 释放初始化申请的堆
 ************************************************************************************************************************************************************************
 */
extern void QueueListDeInit(QUEUE **ppQueue);

/*
 ************************************************************************************************************************************************************************
 *函数名称: QueuePush
 *功能描述: 向队列添加数据
 *输入描述: 队列、节点数据
 *输出描述: 无
 *返回描述: TRUE成功/FALSE失败
 *作者日期: XFW
 *全局声明: 无
 *特殊说明: 无
 ************************************************************************************************************************************************************************
 */
extern uint32_t QueuePush(QUEUE *pQueue, PQUEUE_DATA pReq);




/*
 ************************************************************************************************************************************************************************
 *函数名称: QueueGetHead
 *功能描述: 队列中是否有数据
 *输入描述: 队列
 *输出描述:
 *返回描述: TRUE有数据/FALSE无数据
 *作者日期: XFW
 *全局声明: 无
 *特殊说明:
 ************************************************************************************************************************************************************************
 */
extern uint32_t QueueGetHead(QUEUE *pQueue);

/*
 ************************************************************************************************************************************************************************
 *函数名称: QueuePop
 *功能描述: 从队列中弹出数据
 *输入描述: 队列
 *输出描述: 节点数据
 *返回描述: TRUE成功/FALSE失败
 *作者日期: XFW
 *全局声明: 无
 *特殊说明:
 ************************************************************************************************************************************************************************
 */
extern uint32_t QueuePop(QUEUE *pQueue, PQUEUE_DATA pReq);





#endif

